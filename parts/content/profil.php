<h1>ODEON Ensemble München</h1>
<br>
  Im Jahre 2002 haben sich Mitglieder des Bayerischen Staatsorchesters und der Münchner Philharmoniker sowie Professorinnen/Professoren der Musikhochschulen München und Augsburg unter der Leitung von Michael Hartmann zum ODEON ENSEMBLE MÜNCHEN zusammengefunden. Das Orchester pflegt ein breites Repertoire, das vom Barock über die Spätromantik (Bruckner, Brahms, Mahler) bis zur klassischen Moderne reicht (R. Strauss, Paul Hindemith, Igor Strawinsky). Einen Schwerpunkt bilden die Symphonien und Instrumentalkonzerte der Wiener Klassiker Haydn, Mozart, Beethoven, Schubert.
Mit dem Chor des Odeon Ensembles widmet es sich auch deren geistlichen Werken und bringt alljährlich Passionen und Oratorien von Bach in der Bürgersaalkirche zur Aufführung, die zu einer Institution in München geworden sind. Die Orchestermitglieder sind darüber hinaus regelmäßig als Solisten in kammermusikalischen Besetzungen zu hören. Für die Gesangssolopartien können häufig Solistinnen/en der Bayerischen Staatsoper gewonnen werden. Auch herausragenden Absolventen der Gesangsklassen der Hochschule für Musik und Theater München, die am Beginn einer vielversprechenden Karriere stehen, wird ein Podium geboten.

